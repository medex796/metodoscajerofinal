    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        Random random = new Random();
        this.numerCuenta=random.nextInt (200000000-100000000+1)+100000000;
        boolean bandera = true ;
        while (bandera){
            if (!existe(numerCuenta)){
                bandera = false;
            }
            this.numerCuenta=random.nextInt (200000000-100000000+1)+100000000;

        }

        if (!checkAhorro.isSelected()&&!checkCredito.isSelected()&&!checkDebito.isSelected()){
            JOptionPane.showMessageDialog(null, "Please select the account type ", "Error", JOptionPane.INFORMATION_MESSAGE);

        }else if(checkAhorro.isSelected()&&!checkCredito.isSelected()&&!checkDebito.isSelected()){
            ahorro = true;
            credito = false;
            debito = false;
            siguientepaso("Ahorro");

        }else if(!checkAhorro.isSelected()&&checkCredito.isSelected()&&!checkDebito.isSelected()){
            ahorro = false;
            credito = true;
            debito = false;
            siguientepaso("Credito");

        }else if (!checkAhorro.isSelected()&&!checkCredito.isSelected()&&checkDebito.isSelected()){
            ahorro = false;
            credito = false;
            debito = true;
            siguientepaso("debito");

        }else {
            JOptionPane.showMessageDialog(null, "Please select only one account type. ", "Error", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_jButton5ActionPerformed

